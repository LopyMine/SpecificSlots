    public static Identifier getTexture(Item item) {
        if(!MODS_TEXTURES.isEmpty()){
            for (HashSet<Item> itemList : MODS_TEXTURES.keySet()) {
                if (itemList.contains(item)) {
                    return MODS_TEXTURES.get(itemList);
                }
            }
        }

        if (item == Items.AIR) return null;
        for (HashSet<Item> itemList : ITEM_TEXTURES.keySet()) {
            if (itemList.contains(item)) {
                return ITEM_TEXTURES.get(itemList);
            }
        }

        return ShadowBlocks.getTexture(item);
    }

